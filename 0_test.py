class Stack :
    def __init__(self, capacity) :
        self.stk = [0] * capacity
        self.ptr = 0
    
    def push(self, val) :
        self.stk[self.ptr] = val
        self.ptr += 1

    def pop(self, val=1) :
        if self.ptr > 0 :
            self.ptr -= val

    def empty(self) :
        return True if self.ptr<=0 else False


