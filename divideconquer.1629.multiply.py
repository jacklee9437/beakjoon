from sys import stdin
input = stdin.readline

A, B, C = map(int,input().split())

def cal(A,B,C) :
    if B == 1 :
        return A%C
    if B%2 == 0 :
        return (cal(A,B//2,C) ** 2) % C
    else :
        return (cal(A,B//2,C) ** 2) * A % C

print(cal(A,B,C))

