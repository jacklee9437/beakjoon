for i in range(0,N) :
    if stk.empty() :
        print(0, end=' ')
        stk.push((towers[i], i+1))
        continue
    
    while not stk.empty() :
        if stk.top()[0] < towers[i] :
            stk.pop()
        else :
            print(stk.top()[1], end=' ')
            stk.push((towers[i], i+1))
            break
    else :
        print(0, end=' ')
        stk.push((towers[i], i+1))

    





