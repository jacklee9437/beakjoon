import heapq as hq
from sys import stdin
input = stdin.readline

n = int(input())
people = [tuple(map(int,input().split())) for _ in range(n)]
d = int(input())

hq.heapify(people)
tempHeap = []

line = [people[0][0],people[0][0]+d] # í˜„ì¬ ìŠ¤ìº”í•˜ëŠ” ìœ„ì¹˜
max = 0
cnt = 0


while people :
    while people :
        temp = hq.heappop(people)
        if temp[0] >= line[0] and temp[1] <= line[1] :
            cnt += 1
            if temp[0] != line[0] :
                hq.heappush(tempHeap,temp)
        elif temp[0] > line[0] :
            hq.heappush(people,temp)
        
        if temp[0] > line[1] :
            if cnt > max : 
                max = cnt
                break
            
    else : break
    if tempHeap : 
        line = [tempHeap[0][0], tempHeap[0][0] + d]
        cnt = len(tempHeap)
        while tempHeap[0][0] == line[0] :
            hq.heappop(tempHeap)
            if not tempHeap : break
    else :
        line = [people[0][0], people[0][0] + d]
        cnt = 0
    # print(people)

print(max)
        







