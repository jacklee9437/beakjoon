from sys import stdin, maxsize
input = stdin.readline

N, K = map(int,input().split())
X = tuple(sorted([int(input()) for _ in range(N)]))

pL , pR = 0, N - 1
fullIdx = 0
temp = 0

while pL <= pR :
    pM = (pL + pR) // 2
    
    if X[pM] - X[0] > K :
        pR = pM - 1
        continue
    compTemp = X[pM]*(pM) - sum(X[:pM])
    if compTemp <= K :
        fullIdx = pM
        temp = compTemp
        pL = pM + 1
    else : 
        pR = pM - 1

remainK = K - temp

ableK = remainK // (fullIdx + 1)
T = X[fullIdx] + ableK
print(T)




