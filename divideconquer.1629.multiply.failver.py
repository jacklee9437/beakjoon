from sys import stdin
input = stdin.readline

A, B, C = map(int,input().split())

ans = initRemain = A%C

if B <= C :  
    for i in range(1, B) :
        ans = pow(ans, initRemain) % C
    print(ans)
else :
    remains = [initRemain]
    remain = initRemain
    for i in range(C-1) :
        remain = remain * initRemain % C
        remains.append(remain)
    print(remain[(B-1)%C])
    

