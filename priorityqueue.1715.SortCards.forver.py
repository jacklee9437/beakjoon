import heapq as hq
from sys import stdin
input = stdin.readline

N = int(input())
minHeap = [int(input()) for _ in range(N)]

hq.heapify(minHeap)
compSum = 0

for i in range(N-1) :
    card1 = hq.heappop(minHeap)
    card2 = hq.heappop(minHeap)
    cardBundle = card1 + card2
    hq.heappush(minHeap, cardBundle)
    compSum += cardBundle

print(compSum)
    





